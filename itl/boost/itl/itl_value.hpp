#pragma warning(disable: 4996) //warns against usage of printf other format-functions

namespace itl
{    

template <typename Type>
Type abs(Type val) { return val < 0 ? -val : val; } //JODO


/// static class template for the string representation of values
/**
    <b>value</b> serves as a base to for the representation
    of atomic types and classes as strings.

    The function <tt>toString</tt> is defined for all atomic (built in) types
    like <tt>int, double etc.</tt>. For types other than atomic ones namely
    classes the function template <tt>toString</tt> calls a member function
    <tt>as_string</tt> on the class-type passed to the template.

    Thereby we can implement a general string representation for template classes
    which is independent of the template parameter being an atomic or a class type.
    For every object, including atomic the string converter function toString can
    be called, provides new classes implement a memberfunction <tt>as_string</tt>
  
    @author  Joachim Faulhaber
*/
template <class Type>
struct value
{
    /** String converter for all types <tt>Type</tt>

        E.g.: <tt>int i=5; string s = value<int>::to_string(i);</tt>
    */
    static const std::string to_string(const Type&);

	/** The size of a value is used to be able to order values according to
        their simplicity */
	static std::size_t size(const Type& val);
};


typedef char * CharPT;

#define RETURN_AS_STRING(format, atomicValue) \
    std::stringstream repr; \
    repr << atomicValue; \
    return repr.str();

/* Alternative macro using formated sprintf output
#define RETURN_AS_STRING(format, atomicValue) \
    char stringRepr[512]; \
    sprintf(stringRepr, format, atomicValue); \
    return stringRepr;
*/

template<> inline const std::string value<bool>::to_string(const bool& x){ return x ? "true" : "false"; }
template<> inline const std::string value<char>::to_string(const char& x){ RETURN_AS_STRING("%c", x); }
template<> inline const std::string value<short>::to_string(const short& x) { RETURN_AS_STRING("%d", x); }
template<> inline const std::string value<int>::to_string(const int& x) { RETURN_AS_STRING("%d", x);   }
template<> inline const std::string value<long>::to_string(const long& x) { RETURN_AS_STRING("%ld", x);   }
template<> inline const std::string value<unsigned char>::to_string(const unsigned char& x) { RETURN_AS_STRING("%uc", x);   }
template<> inline const std::string value<unsigned short>::to_string(const unsigned short& x) { RETURN_AS_STRING("%hu", x);   }
template<> inline const std::string value<unsigned int>::to_string(const unsigned int& x) { RETURN_AS_STRING("%u", x);   }
template<> inline const std::string value<unsigned long>::to_string(const unsigned long& x) { RETURN_AS_STRING("%lu", x);   }
template<> inline const std::string value<float>::to_string(const float& x) { RETURN_AS_STRING("%f", x);   }
template<> inline const std::string value<double>::to_string(const double& x) { RETURN_AS_STRING("%lf", x);   }
template<> inline const std::string value<CharPT>::to_string(const CharPT & x) { RETURN_AS_STRING("%s", x);   }
template<> inline const std::string value<std::string>::to_string(const std::string& x) { return x; }

template <class Type>
inline const std::string value<Type>::to_string(const Type& x) { return x.as_string(); }


template<> inline std::size_t value<int>::size(const int& val) { return abs(val); }
template<> inline std::size_t value<double>::size(const double& val) { return static_cast<int>(abs(val)); }

template <typename Type> 
inline std::size_t value<Type>::size(const Type& val){ return static_cast<int>(val.size()); }



} // namespace itl

#endif


